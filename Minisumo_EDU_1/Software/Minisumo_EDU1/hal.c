uint16_t adcGet(uint8_t _gpio){

	uint8_t low, high;

#if defined(analogPinToChannel)
#if defined(__AVR_ATmega32U4__)
	if (pin >= A0) pin -= D_LAST; // allow for channel or pin numbers
#endif
	pin = analogPinToChannel(pin);
#elif defined(__AVR_ATmega1280__) || defined(__AVR_ATmega2560__)
	if (pin >= A0) pin -= D_LAST; // allow for channel or pin numbers
#elif defined(__AVR_ATmega32U4__)
	if (pin >= A0) pin -= D_LAST; // allow for channel or pin numbers
#elif defined(__AVR_ATmega1284__) || defined(__AVR_ATmega1284P__) || defined(__AVR_ATmega644__) || defined(__AVR_ATmega644A__) || defined(__AVR_ATmega644P__) || defined(__AVR_ATmega644PA__)
	if (pin >= A0) pin -= D_LAST; // allow for channel or pin numbers
#else
	if (_gpio >= A0) _gpio -= D_LAST; // allow for channel or pin numbers
#endif

#if defined(ADCSRB) && defined(MUX5)
	// the MUX5 bit of ADCSRB selects whether we're reading from channels
	// 0 to 7 (MUX5 low) or 8 to 15 (MUX5 high).
	ADCSRB = (ADCSRB & ~(1 << MUX5)) | (((pin >> 3) & 0x01) << MUX5);
#endif

	// set the analog reference (high two bits of ADMUX) and select the
	// channel (low 4 bits).  this also sets ADLAR (left-adjust result)
	// to 0 (the default).
#if defined(ADMUX)
#if defined(__AVR_ATtiny25__) || defined(__AVR_ATtiny45__) || defined(__AVR_ATtiny85__)
	ADMUX = (analog_reference << 4) | (pin & 0x07);
#else
	ADMUX = (analog_reference << 6) | (_gpio & 0x07);
#endif
#endif

	// without a delay, we seem to read from the wrong channel
	//delay(1);

#if defined(ADCSRA) && defined(ADCL)
	// start the conversion
	sbi(ADCSRA, ADSC);

	// ADSC is cleared when the conversion finishes
	while (bit_is_set(ADCSRA, ADSC));

	// we have to read ADCL first; doing so locks both ADCL
	// and ADCH until ADCH is read.  reading ADCL second would
	// cause the results of each conversion to be discarded,
	// as ADCL and ADCH would be locked when it completed.
	low  = ADCL;
	high = ADCH;
#else
	// we dont have an ADC, return 0
	low  = 0;
	high = 0;
#endif

	// combine the two bytes
	return (high << 8) | low;
}




void gpioInit(uint8_t _gpio, gpioportmode_t _mode){

	if(_gpio!=NO_GPIO){
		switch(_mode){
			case GPIO_ADC:
			case GPIO_INPUT:
				if(gpio_to_info(_gpio)==0) DDRD&=~(1UL<<gpio_to_num(_gpio));
				if(gpio_to_info(_gpio)==1) DDRB&=~(1UL<<gpio_to_num(_gpio));
				if(gpio_to_info(_gpio)==2) DDRC&=~(1UL<<gpio_to_num(_gpio));
				break;

			case GPIO_PWM:
			case GPIO_OUTPUT:
				if(gpio_to_info(_gpio)==0) DDRD|=(1UL<<gpio_to_num(_gpio));
				if(gpio_to_info(_gpio)==1) DDRB|=(1UL<<gpio_to_num(_gpio));
				if(gpio_to_info(_gpio)==2) DDRC|=(1UL<<gpio_to_num(_gpio));
				break;

			default:
				break;
		}

	}
}



